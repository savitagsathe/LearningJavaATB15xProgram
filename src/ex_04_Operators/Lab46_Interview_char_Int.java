package ex_04_Operators;

public class Lab46_Interview_char_Int {
    static void main(String[] args) {
        char a1='A';//ASCII-65
        char b1='B';//ASCII-66
        System.out.println(a1);//A
        System.out.println(b1);//B

        //char are nothing but int
        System.out.println(a1+b1);//it wont print A+B//output:131(ascii)
        System.out.println(a1-b1);//it wont print A-B//output:-1



    }
}
