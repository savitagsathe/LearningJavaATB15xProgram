package Jan18JavacodingTest;

public class Check_String_Length {
    static void main(String[] args) {
        //Write a Java program to check whether the length of a given string is greater than 10 or not and print the result.
        String s="savita";
        int length=s.length();
        if(s.length()>=10){
            System.out.println(length+" :Length of a given string is greater than 10");
        }
        else {
            System.out.println(length+" :Length of a given string is not greater than 10");

    }

}}
